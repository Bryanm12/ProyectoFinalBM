package Model;


public class ModeloLaberinto {
    private int filas;
    private int columnas;

    public ModeloLaberinto(int filas, int columnas) {
        this.filas = filas;
        this.columnas = columnas;
    }

    public int getFilas() {
        return filas;
    }

    public int getColumnas() {
        return columnas;
    }
    
}
